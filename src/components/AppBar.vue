<template>
  <v-app-bar
    :hide-on-scroll="$vuetify.breakpoint.xsOnly"
    app
    color="light"
    elevate-on-scroll
  >
    <div v-if="$vuetify.breakpoint.smAndUp" class="d-flex align-center">
      <v-icon
        class="mr-3"
        color="green"
        x-large
      >mdi-toy-brick-marker</v-icon>
      <v-app-bar-title>{{ $route.name || 'LEGO Database' }}</v-app-bar-title>
    </div>
    <v-spacer />
    <div
      v-if="$route.meta.withExtensionBar"
      :style="{ width: $vuetify.breakpoint.xsOnly ? '100%' : 'auto' }"
    >
      <v-text-field
        full-width
        label="Search collection"
        v-model="searchText"
        @blur="clearSearch"
        @input="handleSearch"
        clearable
        color="green"
        dense
        flat
        hide-details
        prepend-inner-icon="mdi-magnify"
        single-line
        solo
      />
    </div>
    <template v-slot:extension v-if="$route.meta.withExtensionBar">
      <v-spacer />
      <v-btn
        icon
        @click="handleChangeLayout"
      >
        <v-icon>
          {{ isCardLayout ? 'mdi-view-list' : 'mdi-view-grid' }}
        </v-icon>
      </v-btn>
    </template>
  </v-app-bar>
</template>

<script>
import { eventBus } from '@/main';
import { mapState } from 'vuex';
import { SET_CARD_LAYOUT } from '@/store/types';

export default {
  name: 'AppBar',

  data: () => ({
    isSearch: false,
    searchText: null,
  }),

  methods: {
    handleSearch() {
      eventBus.$emit('changeSearchValue', {
        search: this.searchText,
      });
    },

    clearSearch() {
      this.isSearch = false;
    },

    handleChangeLayout() {
      this.$store.dispatch(SET_CARD_LAYOUT, Number(!this.isCardLayout));
    },
  },

  computed: {
    ...mapState({
      isCardLayout: (state) => state.isCardLayout,
    }),
  },
};
</script>

